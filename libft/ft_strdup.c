#include <stdlib.h>

char	*ft_strdup(const char *s1)
{
	char	*dst;
	int		len;
	int		i;

	len = 0;
	while (s1[len])
		len++;
	if (!(dst = (char *)malloc(sizeof(char) * (len + 1))))
		return (NULL);
	i = 0;
	while (i < len)
	{
		dst[i] = s1[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
