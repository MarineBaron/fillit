#include <unistd.h>
#include "libft.h"

void	ft_putnbr(int n)
{
	long long int	i;
	int				k;
	char			c[20];

	i = n;
	if (i < 0)
	{
		i *= -1;
		write(1, "-", 1);
	}
	k = 0;
	while (i > 9)
	{
		c[k++] = i % 10 + 48;
		i /= 10;
	}
	c[k] = i + 48;
	while (k > -1)
	{
		write(1, &c[k], 1);
		k--;
	}
}
